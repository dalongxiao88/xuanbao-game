package come.tool.Scene.RC;

import org.come.model.Boos;

public class BBBrush
{
    private int d;
    private int z;
    private Boos boos;
    
    public int getD() {
        return this.d;
    }
    
    public void setD(int d) {
        this.d = d;
    }
    
    public int getZ() {
        return this.z;
    }
    
    public void setZ(int z) {
        this.z = z;
    }
    
    public Boos getBoos() {
        return this.boos;
    }
    
    public void setBoos(Boos boos) {
        this.boos = boos;
    }
}
