package org.come.model;

public class InitFly
{
    private String raceid;
    private String flyid;
    private String flyname;
    private String flystate;
    private String flylvl;
    private String live;
    private String skin;
    private String exp;
    
    public String getRaceid() {
        return this.raceid;
    }
    
    public void setRaceid(String raceid) {
        this.raceid = raceid;
    }
    
    public String getflyid() {
        return this.flyid;
    }
    
    public String getSkin() {
        return this.skin;
    }
    
    public void setSkin(String skin) {
        this.skin = skin;
    }
    
    public void setflyid(String flyid) {
        this.flyid = flyid;
    }
    
    public String getflyname() {
        return this.flyname;
    }
    
    public String getFlystate() {
        return this.flystate;
    }
    
    public void setFlystate(String flystate) {
        this.flystate = flystate;
    }
    
    public void setflyname(String flyname) {
        this.flyname = flyname;
    }
    
    public String getflylvl() {
        return this.flylvl;
    }
    
    public void setflylvl(String flylvl) {
        this.flylvl = flylvl;
    }
    
    public String getLive() {
        return this.live;
    }
    
    public void setLive(String live) {
        this.live = live;
    }
    
    public String getExp() {
        return this.exp;
    }
    
    public void setExp(String exp) {
        this.exp = exp;
    }
}
