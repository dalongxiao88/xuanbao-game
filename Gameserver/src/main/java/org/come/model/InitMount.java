package org.come.model;

public class InitMount
{
    private String raceid;
    private String mountid;
    private String mountname;
    private String mountlvl;
    private String live;
    private String spri;
    private String power;
    private String bone;
    private String exp;
    
    public String getRaceid() {
        return this.raceid;
    }
    
    public void setRaceid(String raceid) {
        this.raceid = raceid;
    }
    
    public String getMountid() {
        return this.mountid;
    }
    
    public void setMountid(String mountid) {
        this.mountid = mountid;
    }
    
    public String getMountname() {
        return this.mountname;
    }
    
    public void setMountname(String mountname) {
        this.mountname = mountname;
    }
    
    public String getMountlvl() {
        return this.mountlvl;
    }
    
    public void setMountlvl(String mountlvl) {
        this.mountlvl = mountlvl;
    }
    
    public String getLive() {
        return this.live;
    }
    
    public void setLive(String live) {
        this.live = live;
    }
    
    public String getSpri() {
        return this.spri;
    }
    
    public void setSpri(String spri) {
        this.spri = spri;
    }
    
    public String getPower() {
        return this.power;
    }
    
    public void setPower(String power) {
        this.power = power;
    }
    
    public String getBone() {
        return this.bone;
    }
    
    public void setBone(String bone) {
        this.bone = bone;
    }
    
    public String getExp() {
        return this.exp;
    }
    
    public void setExp(String exp) {
        this.exp = exp;
    }
}
