package org.come.bean;

public class ConfirmBean
{
    private int type;
    private String MSG;
    private String value;
    
    public int getType() {
        return this.type;
    }
    
    public void setType(int type) {
        this.type = type;
    }
    
    public String getMSG() {
        return this.MSG;
    }
    
    public void setMSG(String mSG) {
        this.MSG = mSG;
    }
    
    public String getValue() {
        return this.value;
    }
    
    public void setValue(String value) {
        this.value = value;
    }
}
