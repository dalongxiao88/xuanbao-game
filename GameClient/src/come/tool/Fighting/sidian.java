package come.tool.Fighting;

public class sidian
{
    private int x;
    private int y;
    private int z;
    private int w;
    
    public void setsidian(int x, int y, int z, int w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
    
    public sidian(int x, int y, int z, int w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
    
    public int getX() {
        return this.x;
    }
    
    public void setX(int x) {
        this.x = x;
    }
    
    public int getY() {
        return this.y;
    }
    
    public void setY(int y) {
        this.y = y;
    }
    
    public int getZ() {
        return this.z;
    }
    
    public void setZ(int z) {
        this.z = z;
    }
    
    public int getW() {
        return this.w;
    }
    
    public void setW(int w) {
        this.w = w;
    }
}
