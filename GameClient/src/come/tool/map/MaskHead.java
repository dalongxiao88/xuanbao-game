package come.tool.map;

public class MaskHead
{
    public int unknown;
    public int maskNum;
    public int[] maskList;
    
    public int getUnknown() {
        return this.unknown;
    }
    
    public void setUnknown(int unknown) {
        this.unknown = unknown;
    }
    
    public int getMaskNum() {
        return this.maskNum;
    }
    
    public void setMaskNum(int maskNum) {
        this.maskNum = maskNum;
    }
    
    public int[] getMaskList() {
        return this.maskList;
    }
    
    public void setMaskList(int[] maskList) {
        this.maskList = maskList;
    }
}
