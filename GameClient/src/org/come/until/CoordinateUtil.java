package org.come.until;

public class CoordinateUtil {

	public int x;
	public int y;
	
	
	public void setCoordinateUtil(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
