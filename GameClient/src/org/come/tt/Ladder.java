package org.come.tt;

import java.awt.Color;

public class Ladder
{
    private String level;
    private Color color;
    
    public String getLevel() {
        return this.level;
    }
    
    public void setLevel(String level) {
        this.level = level;
    }
    
    public Color getColor() {
        return this.color;
    }
    
    public void setColor(Color color) {
        this.color = color;
    }
}
