package org.come.model;

public class aCard
{
    private int id;
    private int type;
    private String skin;
    private String gn;
    private int cardType;
    private int qhl;
    private String zz;
    private String name;
    private int time;
    private int money;
    private String value;
    
    public int getId() {
        return this.id;
    }
    
    public void setId(int id) {
        this.id = id;
    }
    
    public int getType() {
        return this.type;
    }
    
    public void setType(int type) {
        this.type = type;
    }
    
    public String getSkin() {
        return this.skin;
    }
    
    public void setSkin(String skin) {
        this.skin = skin;
    }
    
    public String getGn() {
        return this.gn;
    }
    
    public void setGn(String gn) {
        this.gn = gn;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public int getTime() {
        return this.time;
    }
    
    public void setTime(int time) {
        this.time = time;
    }
    
    public int getMoney() {
        return this.money;
    }
    
    public void setMoney(int money) {
        this.money = money;
    }
    
    public String getValue() {
        return this.value;
    }
    
    public void setValue(String value) {
        this.value = value;
    }
    
    public int getCardType() {
        return this.cardType;
    }
    
    public void setCardType(int cardType) {
        this.cardType = cardType;
    }
    
    public int getQhl() {
        return this.qhl;
    }
    
    public void setQhl(int qhl) {
        this.qhl = qhl;
    }
    
    public String getZz() {
        return this.zz;
    }
    
    public void setZz(String zz) {
        this.zz = zz;
    }
}
