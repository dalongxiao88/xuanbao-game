package org.come.model;

public class PalData
{
    private int palId;
    private String name;
    private String skin;
    private int hp;
    private int mp;
    private int ap;
    private int sp;
    private String kx;
    private int[] jds;
    private String skill;
    private String xh;
    private String trait;
    
    public int getPalId() {
        return this.palId;
    }
    
    public void setPalId(int palId) {
        this.palId = palId;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public String getSkin() {
        return this.skin;
    }
    
    public void setSkin(String skin) {
        this.skin = skin;
    }
    
    public int getHp() {
        return this.hp;
    }
    
    public void setHp(int hp) {
        this.hp = hp;
    }
    
    public int getMp() {
        return this.mp;
    }
    
    public void setMp(int mp) {
        this.mp = mp;
    }
    
    public int getAp() {
        return this.ap;
    }
    
    public void setAp(int ap) {
        this.ap = ap;
    }
    
    public int getSp() {
        return this.sp;
    }
    
    public void setSp(int sp) {
        this.sp = sp;
    }
    
    public String getKx() {
        return this.kx;
    }
    
    public void setKx(String kx) {
        this.kx = kx;
    }
    
    public int[] getJds() {
        return this.jds;
    }
    
    public void setJds(int[] jds) {
        this.jds = jds;
    }
    
    public String getSkill() {
        return this.skill;
    }
    
    public void setSkill(String skill) {
        this.skill = skill;
    }
    
    public String getXh() {
        return this.xh;
    }
    
    public void setXh(String xh) {
        this.xh = xh;
    }
    
    public String getTrait() {
        return this.trait;
    }
    
    public void setTrait(String trait) {
        this.trait = trait;
    }
}
