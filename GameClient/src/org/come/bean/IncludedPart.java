package org.come.bean;

public class IncludedPart
{
    private String partid;
    private int number;
    
    public String getPartid() {
        return this.partid;
    }
    
    public void setPartid(String partid) {
        this.partid = partid;
    }
    
    public int getNumber() {
        return this.number;
    }
    
    public void setNumber(int number) {
        this.number = number;
    }
}
