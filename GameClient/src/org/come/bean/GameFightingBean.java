package org.come.bean;

public class GameFightingBean
{
    private String location;
    private int beanFightint;
    private int fightingLocation;
    
    public String getLocation() {
        return this.location;
    }
    
    public void setLocation(String location) {
        this.location = location;
    }
    
    public int getBeanFightint() {
        return this.beanFightint;
    }
    
    public void setBeanFightint(int beanFightint) {
        this.beanFightint = beanFightint;
    }
    
    public int getFightingLocation() {
        return this.fightingLocation;
    }
    
    public void setFightingLocation(int fightingLocation) {
        this.fightingLocation = fightingLocation;
    }
}
