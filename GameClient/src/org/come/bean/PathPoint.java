package org.come.bean;

public class PathPoint
{
    private int x;
    private int y;
    private int y1;
    private int y2;
    private int y3;
    
    public PathPoint() {
    }
    
    public PathPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return this.x;
    }
    
    public void setX(int x) {
        this.x = x;
    }
    
    public int getY() {
        return this.y;
    }
    
    public void setY(int y) {
        this.y = y;
    }
    
    public int getY1() {
        return this.y1;
    }
    
    public void setY1(int y1) {
        this.y1 = y1;
    }
    
    public int getY2() {
        return this.y2;
    }
    
    public void setY2(int y2) {
        this.y2 = y2;
    }
    
    public int getY3() {
        return this.y3;
    }
    
    public void setY3(int y3) {
        this.y3 = y3;
    }
}
