package org.come.bean;

public class ChangeMapBean
{
    private String mapid;
    private int mapx;
    private int mapy;
    private int type;
    
    public String getMapid() {
        return this.mapid;
    }
    
    public void setMapid(String mapid) {
        this.mapid = mapid;
    }
    
    public int getMapx() {
        return this.mapx;
    }
    
    public void setMapx(int mapx) {
        this.mapx = mapx;
    }
    
    public int getMapy() {
        return this.mapy;
    }
    
    public void setMapy(int mapy) {
        this.mapy = mapy;
    }
    
    public int getType() {
        return this.type;
    }
    
    public void setType(int type) {
        this.type = type;
    }
}
