package org.come.bean;

public class TestBean
{
    private int id;
    private String test;
    private String name;
    
    public int getId() {
        return this.id;
    }
    
    public void setId(int id) {
        this.id = id;
    }
    
    public String getTest() {
        return this.test;
    }
    
    public void setTest(String test) {
        this.test = test;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}
